export enum FormElementType {
  /** Input Filter type */
  input,

  /** Select Filter type, just a regular select dropdown. You might want to try "singleSelect" which has a nicer look and feel. */
  select,

  /** Multiple-Select Filter type */
  multipleSelect,

  /** Single Filter type */
  singleSelect,

  /** Custom Filter type */
  custom,

  /** TextArea element type */
  textarea
}
